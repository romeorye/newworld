	@RequestMapping(value="/knld/pub/getEduInfo.do")
	public ModelAndView getEduInfo(
			@RequestParam HashMap<String, Object> input,
			HttpServletRequest request,
			HttpServletResponse response,
			HttpSession session,
			ModelMap model
			){

		/* 반드시 공통 호출 후 작업 */
		checkSessionObjRUI(input, session, model);
		input = StringUtil.toUtf8(input);

		LOGGER.debug("###########################################################");
		LOGGER.debug("EduController - getEduInfo [교육세미나 상세]");
		LOGGER.debug("input = > " + input);
		LOGGER.debug("###########################################################");

		ModelAndView modelAndView = new ModelAndView("ruiView");

		if(NullUtil.isNull(input.get("_userId")) ||  NullUtil.nvl(input.get("_userId"), "").equals("-1")  ){
			String rtnMsg = messageSourceAccessor.getMessage("msg.alert.sessionTimeout");
			SayMessage.setMessage(rtnMsg);
			modelAndView.addObject("eduDataSet", null);
        } else {
        	// 교육세미나 상세 조회
	        Map<String,Object> resultMap = eduService.getEduInfo(input);
			modelAndView.addObject("eduInfoDataSet", RuiConverter.createDataset("eduInfoDataSet", resultMap));
        }
		return modelAndView;
	}

