	/** 등록 **/
	@RequestMapping(value="/knld/pub/insertEduInfo.do")
	public ModelAndView insertEduInfo(
			@RequestParam HashMap<String, String> input,
			HttpServletRequest request,
			HttpSession session,
			ModelMap model
			){
		LOGGER.debug("###########################################################");
		LOGGER.debug("EduController - insertEduInfo [교육세미나 저장&업데이트]");
		LOGGER.debug("input = > " + input);
		LOGGER.debug("###########################################################");

		checkSessionRUI(input, session, model);
		ModelAndView modelAndView = new ModelAndView("ruiView");
		HashMap<String, Object> rtnMeaasge = new HashMap<String, Object>();

		// 결과메시지
		rtnMeaasge.put("rtnSt", "S");
		rtnMeaasge.put("rtnMsg", "저장 되었습니다.");
		rtnMeaasge.put("eduId", "");

		int totCnt = 0;    							//전체건수
		List<Map<String, Object>> eduRgstDataSetList;	//교육세미나 변경데이터

		try{
			//교육세미나 저장&수정
			String eduId = "";
			eduRgstDataSetList = RuiConverter.convertToDataSet(request,"eduRgstDataSet");

			for(Map<String,Object> eduRgstDataSetMap : eduRgstDataSetList) {
				eduRgstDataSetMap.put("_userId" , NullUtil.nvl(input.get("_userId"), ""));
				eduService.insertEduInfo(eduRgstDataSetMap);
				totCnt++;
			}

			rtnMeaasge.put("eduId", eduId);

			if(totCnt == 0 ) {
				rtnMeaasge.put("rtnSt", "F");
				rtnMeaasge.put("rtnMsg", "변경된 내용이 없습니다.");
			}
		} catch (Exception e) {
			e.printStackTrace();
			rtnMeaasge.put("rtnSt", "F");
			rtnMeaasge.put("rtnMsg", "저장이 실패하였습니다.");
		}

		model.addAttribute("inputData", input);
		modelAndView.addObject("resultDataSet", RuiConverter.createDataset("resultDataSet", rtnMeaasge));
		return modelAndView;
	}

